    fn ec_p256_spki_gate_fails_closed_on_junk() {
        assert!(!is_ec_p256_spki(b"not der"));
        assert!(!is_ec_p256_spki(&[0x30, 0x59]));
    }

    #[test]
    fn spki_matches_its_own_prefix() {
        for (cert, spki) in [
            (TEST_CERT_A_DER, TEST_SPKI_A_DER),
            (TEST_CERT_B_DER, TEST_SPKI_B_DER),
        ] {
            let prefix = sha256(spki)[..16].to_vec();
            assert!(spki_matches_prefix(cert, &prefix));
            assert!(chain_spki_matches_prefix(&[cert.to_vec()], &prefix));
        }
    }

    #[test]
    fn spki_wrong_prefix_does_not_match() {
        assert!(!spki_matches_prefix(TEST_CERT_A_DER, &[0x00; 16]));
    }

    #[test]
    fn spki_empty_or_overlong_prefix_fails_closed() {
        assert!(!spki_matches_prefix(TEST_CERT_A_DER, &[]));
        assert!(!spki_matches_prefix(TEST_CERT_A_DER, &[0u8; 33]));
    }

    #[test]
    fn direct_and_spki_pins_do_not_cross_match() {
        for (cert, spki) in [
            (TEST_CERT_A_DER, TEST_SPKI_A_DER),
            (TEST_CERT_B_DER, TEST_SPKI_B_DER),
        ] {
            let cert_der_prefix = sha256(cert)[..16].to_vec();
            let spki_prefix = sha256(spki)[..16].to_vec();
            assert!(!cert_matches_prefix(cert, &spki_prefix));
            assert!(!spki_matches_prefix(cert, &cert_der_prefix));
        }
    }

    #[test]
    fn extracts_tbs_and_signature_from_real_certificate() {
        for cert in [TEST_CERT_A_DER, TEST_CERT_B_DER] {
            let (tbs, signature) = extract_tbs_and_signature(cert).unwrap();
            assert_eq!(tbs.first(), Some(&0x30));
            assert!(!signature.is_empty());
        }
    }

    #[test]
    fn distinct_static_vectors_do_not_cross_match() {
        let cert_a_prefix = sha256(TEST_CERT_A_DER);
        let cert_b_prefix = sha256(TEST_CERT_B_DER);
        assert!(!cert_matches_prefix(TEST_CERT_A_DER, &cert_b_prefix[..16]));
        assert!(!cert_matches_prefix(TEST_CERT_B_DER, &cert_a_prefix[..16]));

        let spki_a_prefix = sha256(TEST_SPKI_A_DER);
        let spki_b_prefix = sha256(TEST_SPKI_B_DER);
        assert!(!spki_matches_prefix(TEST_CERT_A_DER, &spki_b_prefix[..16]));
        assert!(!spki_matches_prefix(TEST_CERT_B_DER, &spki_a_prefix[..16]));
    }

    #[test]
    fn tbs_and_signature_extraction_fails_closed_on_malformed_der() {
        for cert in [
            TRUNCATED_CERT_DER,
            CERT_WITH_TRAILING_DATA,
            CERT_WITH_BAD_SIGNATURE_BIT_STRING,
        ] {
            assert_eq!(extract_tbs_and_signature(cert), Err(CaError::MalformedDer));
        }
    }
